package com.ericsson.cifwk.presentation.dto;

import com.google.common.collect.Sets;

import java.util.LinkedHashSet;

public class SimpleTestCaseInfo{

    private Long id;

    private String title;

    private LinkedHashSet<String> requirementIds = Sets.newLinkedHashSet();

    private String testCaseId;

    private String description;

    private Long sequenceNumber;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public LinkedHashSet<String> getRequirementIds() {
        return requirementIds;
    }

    public void setRequirementIds(LinkedHashSet<String> requirementIds) {
        this.requirementIds = requirementIds;
    }

    public String getTestCaseId() {
        return testCaseId;
    }

    public void setTestCaseId(String testCaseId) {
        this.testCaseId = testCaseId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Long getSequenceNumber() {
        return sequenceNumber;
    }

    public void setSequenceNumber(Long sequenceNumber) {
        this.sequenceNumber = sequenceNumber;
    }
}
